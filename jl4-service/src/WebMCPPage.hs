  , "      }"
  , ""
  , "      window.dispatchEvent(new CustomEvent('webmcp-ready', {"
  , "        detail: { functions: allFns, deployments: deploymentInfos, webmcpRegistered: webmcpRegistered }"
  , "      }));"
  , "    })"
