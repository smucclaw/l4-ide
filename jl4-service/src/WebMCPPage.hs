  , "  'use strict';"
  , ""
  , "  var scriptTag = document.currentScript || document.querySelector('script[src*=\"embed.js\"]');"
  , "  var apiKey = scriptTag && scriptTag.getAttribute('data-api-key');"
  , "  var scope = scriptTag && scriptTag.getAttribute('data-scope');"
  , "  var toolsMode = (scriptTag && scriptTag.getAttribute('data-tools')) || 'auto';"
  , "  var baseUrl = scriptTag && scriptTag.src ? new URL(scriptTag.src).origin : window.location.origin;"
  , ""
  , "  var authHeaders = apiKey ? { 'Authorization': 'Bearer ' + apiKey } : {};"
  , "  var fetchOpts = apiKey ? {} : { credentials: 'include' };"
  , ""
  , "  function doFetch(url, opts) {"
  , "    return fetch(url, Object.assign({}, fetchOpts, { headers: Object.assign({}, authHeaders, (opts && opts.headers) || {}) }, opts || {}));"
  , "  }"
  , ""
  , ""
  , "  function sanitizeName(name, maxLen) {"
  , "    var s = name.replace(/[^a-zA-Z0-9_.-]/g, '-').replace(/-+/g, '-').replace(/^-|-$/g, '');"
  , "    if (!s || !/^[a-zA-Z_]/.test(s)) s = '_' + s;"
  , "    return s.substring(0, maxLen || 64);"
  , "  }"
  , ""
  , "  // Sanitize a property name: same replacement char (-) as sanitizeName"
  , "  function sanitizePropName(name) {"
  , "    var s = name.replace(/[^a-zA-Z0-9_.-]/g, '-').replace(/-+/g, '-').replace(/^-|-$/g, '');"
  , "    return s || '_unnamed';"
  , "  }"
  , ""
  , "  // Sanitize all property names in a parameters schema and return"
  , "  // { schema, reverseMap } where reverseMap maps sanitized -> original"
  , "  function sanitizeParamsSchema(params) {"
  , "    if (!params || !params.properties) return { schema: params || { type: 'object', properties: {}, required: [] }, reverseMap: {} };"
  , "    var newProps = {}, reverseMap = {};"
  , "    Object.keys(params.properties).forEach(function(origKey) {"
  , "      var safKey = sanitizePropName(origKey);"
  , "      newProps[safKey] = sanitizeParamValue(params.properties[origKey]);"
  , "      if (safKey !== origKey) reverseMap[safKey] = origKey;"
  , "    });"
  , "    var newRequired = (params.required || []).map(sanitizePropName);"
  , "    return {"
  , "      schema: { type: 'object', properties: newProps, required: newRequired },"
  , "      reverseMap: reverseMap"
  , "    };"
  , "  }"
  , ""
  , "  // Recursively sanitize nested property names inside a parameter value"
  , "  function sanitizeParamValue(p) {"
  , "    if (!p) return p;"
  , "    var out = { type: p.type, alias: p.alias, description: p.description };"
  , "    if (p['enum'] && p['enum'].length) out['enum'] = p['enum'];"
  , "    if (p.format) out.format = p.format;"
  , "    if (p.properties) {"
  , "      var nested = {};"
  , "      Object.keys(p.properties).forEach(function(k) {"
  , "        nested[sanitizePropName(k)] = sanitizeParamValue(p.properties[k]);"
  , "      });"
  , "      out.properties = nested;"
  , "    }"
  , "    if (p.propertyOrder) out.propertyOrder = p.propertyOrder.map(sanitizePropName);"
  , "    if (p.items) out.items = sanitizeParamValue(p.items);"
  , "    return out;"
  , "  }"
  , ""
  , "  // Remap sanitized argument keys back to original L4 names"
  , "  function remapArgs(args, reverseMap) {"
  , "    var out = {};"
  , "    Object.keys(args).forEach(function(k) {"
  , "      out[reverseMap[k] || k] = args[k];"
  , "    });"
  , "    return out;"
  , "  }"
  , ""
  , "  // Track all registered tool names to avoid collisions"
  , "  var registeredNames = {};"
  , ""
  , "  // Build unique tool names: function-name only, with .deployment-prefix on collision"
  , "  function buildToolNames(fns) {"
  , "    var MAX = 60;"
  , "    // Group by sanitized function name to detect collisions"
  , "    var groups = {};"
  , "    fns.forEach(function(fn) {"
  , "      var key = sanitizeName(fn.name, MAX);"
  , "      if (!groups[key]) groups[key] = [];"
  , "      groups[key].push(fn);"
  , "    });"
  , "    var result = [];"
  , "    Object.keys(groups).forEach(function(key) {"
  , "      var group = groups[key];"
  , "      if (group.length === 1 && !registeredNames[key]) {"
  , "        result.push({ fn: group[0], toolName: key });"
  , "      } else {"
  , "        // Collision: find shortest unique deployment prefix for each"
  , "        var deps = group.map(function(fn) { return sanitizeName(fn.deployment, MAX); });"
  , "        group.forEach(function(fn, i) {"
