{-# LANGUAGE OverloadedStrings #-}

module WebMCPPage (
  RawHtml (..),
  RawJs (..),
  HTML,
  JavaScript,
  renderOrgWebMCPScript,
) where

import Data.ByteString.Lazy (ByteString)
import qualified Data.ByteString.Lazy.Char8 as LBS
import qualified Data.Text as Text
import qualified Data.Text.Encoding as Text.Encoding
import Network.HTTP.Media ((//), (/:))
import Servant (Accept (..), MimeRender (..))

-- | HTML content type for Servant.
data HTML

instance Accept HTML where
  contentType _ = "text" // "html" /: ("charset", "utf-8")

-- | JavaScript content type for Servant.
data JavaScript

instance Accept JavaScript where
  contentType _ = "application" // "javascript" /: ("charset", "utf-8")

-- | Raw HTML response.
newtype RawHtml = RawHtml { unRawHtml :: ByteString }

instance MimeRender HTML RawHtml where
  mimeRender _ (RawHtml bs) = bs

-- | Raw JavaScript response.
newtype RawJs = RawJs { unRawJs :: ByteString }

instance MimeRender JavaScript RawJs where
  mimeRender _ (RawJs bs) = bs

-- | Render the org-wide WebMCP JavaScript.
-- Fetches all deployments, applies scope filtering, registers tools.
renderOrgWebMCPScript :: RawJs
renderOrgWebMCPScript = RawJs $ LBS.fromStrict $ Text.Encoding.encodeUtf8 $ Text.unlines
  [ "(function() {"
  , "  'use strict';"
  , ""
  , "  var scriptTag = document.currentScript || document.querySelector('script[src*=\"embed.js\"]');"
  , "  var apiKey = scriptTag && scriptTag.getAttribute('data-api-key');"
  , "  var scope = scriptTag && scriptTag.getAttribute('data-scope');"
  , "  var toolsMode = (scriptTag && scriptTag.getAttribute('data-tools')) || 'auto';"
  , "  var baseUrl = scriptTag && scriptTag.src ? new URL(scriptTag.src).origin : window.location.origin;"
  , ""
  , "  var authHeaders = apiKey ? { 'Authorization': 'Bearer ' + apiKey } : {};"
  , "  var fetchOpts = apiKey ? {} : { credentials: 'include' };"
  , ""
  , "  function doFetch(url, opts) {"
  , "    return fetch(url, Object.assign({}, fetchOpts, { headers: Object.assign({}, authHeaders, (opts && opts.headers) || {}) }, opts || {}));"
  , "  }"
  , ""
  , ""
  , "  function sanitizeName(name, maxLen) {"
  , "    var s = name.replace(/[^a-zA-Z0-9_.-]/g, '-').replace(/-+/g, '-').replace(/^-|-$/g, '');"
  , "    if (!s || !/^[a-zA-Z_]/.test(s)) s = '_' + s;"
  , "    return s.substring(0, maxLen || 60);"
  , "  }"
  , ""
  , "  // Sanitize a property name: same replacement char (-) as sanitizeName"
  , "  function sanitizePropName(name) {"
  , "    var s = name.replace(/[^a-zA-Z0-9_.-]/g, '-').replace(/-+/g, '-').replace(/^-|-$/g, '');"
  , "    return s || '_unnamed';"
  , "  }"
  , ""
  , "  // Sanitize all property names in a parameters schema and return"
  , "  // { schema, reverseMap } where reverseMap maps sanitized -> original"
  , "  // The reverseMap includes all nested property names recursively."
  , "  function sanitizeParamsSchema(params) {"
  , "    if (!params || !params.properties) return { schema: params || { type: 'object', properties: {}, required: [] }, reverseMap: {} };"
  , "    var newProps = {}, reverseMap = {};"
  , "    Object.keys(params.properties).forEach(function(origKey) {"
  , "      var safKey = sanitizePropName(origKey);"
  , "      newProps[safKey] = sanitizeParamValue(params.properties[origKey], reverseMap);"
  , "      if (safKey !== origKey) reverseMap[safKey] = origKey;"
  , "    });"
  , "    var newRequired = (params.required || []).map(sanitizePropName);"
  , "    return {"
  , "      schema: { type: 'object', properties: newProps, required: newRequired },"
  , "      reverseMap: reverseMap"
  , "    };"
  , "  }"
  , ""
  , "  // Recursively sanitize nested property names inside a parameter value."
  , "  // Accumulates sanitized -> original mappings into reverseMap."
  , "  function sanitizeParamValue(p, reverseMap) {"
  , "    if (!p) return p;"
  , "    var out = { type: p.type, alias: p.alias, description: p.description };"
  , "    if (p['enum'] && p['enum'].length) out['enum'] = p['enum'];"
  , "    if (p.format) out.format = p.format;"
  , "    if (p.properties) {"
  , "      var nested = {};"
  , "      Object.keys(p.properties).forEach(function(k) {"
  , "        var safKey = sanitizePropName(k);"
  , "        nested[safKey] = sanitizeParamValue(p.properties[k], reverseMap);"
  , "        if (safKey !== k) reverseMap[safKey] = k;"
  , "      });"
  , "      out.properties = nested;"
  , "    }"
  , "    if (p.propertyOrder) out.propertyOrder = p.propertyOrder.map(sanitizePropName);"
  , "    if (p.items) out.items = sanitizeParamValue(p.items, reverseMap);"
  , "    return out;"
  , "  }"
  , ""
  , "  // Remap sanitized argument keys back to original L4 names."
  , "  // Recursively remaps keys in nested objects and arrays."
  , "  function remapArgs(args, reverseMap) {"
  , "    if (Array.isArray(args)) return args.map(function(v) { return remapArgs(v, reverseMap); });"
  , "    if (args && typeof args === 'object') {"
  , "      var out = {};"
  , "      Object.keys(args).forEach(function(k) {"
  , "        out[reverseMap[k] || k] = remapArgs(args[k], reverseMap);"
  , "      });"
  , "      return out;"
  , "    }"
  , "    return args;"
  , "  }"
  , ""
  , "  // Track all registered tool names to avoid collisions"
  , "  var registeredNames = {};"
  , ""
  , "  // Build unique tool names: function-name only, with .deployment-prefix on collision"
  , "  function buildToolNames(fns) {"
  , "    var MAX = 60;"
  , "    // Group by sanitized function name to detect collisions"
  , "    var groups = {};"
  , "    fns.forEach(function(fn) {"
  , "      var key = sanitizeName(fn.name, MAX);"
  , "      if (!groups[key]) groups[key] = [];"
  , "      groups[key].push(fn);"
  , "    });"
  , "    var result = [];"
  , "    Object.keys(groups).forEach(function(key) {"
  , "      var group = groups[key];"
  , "      if (group.length === 1 && !registeredNames[key]) {"
  , "        result.push({ fn: group[0], toolName: key });"
  , "      } else {"
  , "        // Collision: find shortest unique deployment prefix for each"
  , "        var deps = group.map(function(fn) { return sanitizeName(fn.deployment, MAX); });"
  , "        group.forEach(function(fn, i) {"
  , "          var dep = deps[i];"
  , "          var others = deps.filter(function(_, j) { return j !== i; });"
  , "          // Find shortest prefix of dep that distinguishes from all others"
  , "          var prefixLen = 1;"
  , "          while (prefixLen < dep.length) {"
  , "            var prefix = dep.substring(0, prefixLen);"
  , "            var unique = others.every(function(o) { return o.substring(0, prefixLen) !== prefix; });"
  , "            if (unique) break;"
  , "            prefixLen++;"
  , "          }"
  , "          var suffix = '.' + dep.substring(0, prefixLen);"
  , "          var baseName = sanitizeName(fn.name, MAX - suffix.length);"
  , "          result.push({ fn: fn, toolName: baseName + suffix });"
  , "        });"
  , "      }"
  , "    });"
  , "    return result;"
  , "  }"
  , ""
  , "  // Single fetch: org-level openapi.json with server-side scope filtering"
  , "  var openApiUrl = baseUrl + '/openapi.json';"
  , "  if (scope) openApiUrl += '?scope=' + encodeURIComponent(scope);"
  , ""
  , "  doFetch(openApiUrl)"
  , "    .then(function(r) { if (!r.ok) throw new Error('HTTP ' + r.status); return r.json(); })"
  , "    .then(function(data) {"
  , "      var allFns = (data.functions || []).map(function(fn) {"
  , "        return {"
  , "          deployment: fn.deployment,"
  , "          name: fn.name,"
  , "          description: fn.description || '',"
  , "          returnType: fn.returnType || 'unknown',"
  , "          section: fn.section || null,"
  , "          isDeontic: fn.isDeontic || false,"
  , "          parameters: fn.parameters,"
  , "          parameterNames: Object.keys((fn.parameters && fn.parameters.properties) || {})"
  , "        };"
  , "      });"
  , ""
  , "      // Derive deployment list from functions"
  , "      var depMap = {};"
  , "      allFns.forEach(function(fn) {"
  , "        if (!depMap[fn.deployment]) depMap[fn.deployment] = { id: fn.deployment, status: 'ready' };"
  , "      });"
  , "      var deploymentInfos = Object.keys(depMap).map(function(k) { return depMap[k]; });"
  , ""
  , "      // Register tools"
  , "      var webmcpRegistered = false;"
  , "      if (typeof navigator !== 'undefined' && 'modelContext' in navigator) {"
  , "        var wantDiscovery = toolsMode === 'auto' || toolsMode === 'discovery' || toolsMode === 'all';"
  , "        var wantDirect = toolsMode === 'direct' || toolsMode === 'all' || (toolsMode === 'auto' && allFns.length <= 20);"
  , ""
  , "        if (wantDiscovery) registerDiscoveryTools(baseUrl, allFns);"
  , "        if (wantDirect) registerDirectTools(baseUrl, allFns);"
  , "        webmcpRegistered = true;"
  , "      }"
  , ""
  , "      window.dispatchEvent(new CustomEvent('webmcp-ready', {"
  , "        detail: { functions: allFns, deployments: deploymentInfos, webmcpRegistered: webmcpRegistered }"
  , "      }));"
  , "    })"
  , "    .catch(function(e) {"
  , "      console.error('WebMCP: failed to load deployments', e);"
  , "      window.dispatchEvent(new CustomEvent('webmcp-error', { detail: { error: e.message } }));"
  , "    });"
  , ""
  , "  function registerDiscoveryTools(baseUrl, fns) {"
  , "    var index = fns.map(function(fn) {"
  , "      return { deployment: fn.deployment, name: fn.name, description: fn.description, returnType: fn.returnType, section: fn.section, isDeontic: fn.isDeontic, parameterNames: fn.parameterNames };"
  , "    });"
  , ""
  , "    function regTool(def) { registeredNames[def.name] = true; navigator.modelContext.registerTool(def); }"
  , ""
  , "    regTool({"
  , "      name: 'search_rules',"
  , "      description: 'Search ' + fns.length + ' available legal rules by keyword. Returns matching deployment, name, description, return type, and parameter names.',"
  , "      inputSchema: { type: 'object', properties: { query: { type: 'string', description: 'Search keyword' } }, required: ['query'] },"
  , "      execute: function(args) {"
  , "        var words = (args.query || '').toLowerCase().split(/\\s+/).filter(Boolean);"
  , "        var matches = index.filter(function(fn) {"
  , "          var hay = (fn.name + ' ' + fn.description + ' ' + fn.deployment + ' ' + fn.parameterNames.join(' ')).toLowerCase();"
  , "          return words.every(function(w) { return hay.split(/\\s+/).some(function(t) { return t.indexOf(w) === 0 || w.indexOf(t) === 0; }); });"
  , "        });"
  , "        return { content: [{ type: 'text', text: JSON.stringify(matches, null, 2) }] };"
  , "      }"
  , "    });"
  , ""
  , "    regTool({"
  , "      name: 'get_rule_schema',"
  , "      description: 'Get the full parameter schema for a specific rule.',"
  , "      inputSchema: { type: 'object', properties: { deployment: { type: 'string', description: 'Deployment ID' }, function_name: { type: 'string', description: 'Function name' } }, required: ['deployment', 'function_name'] },"
  , "      execute: function(args) {"
  , "        return doFetch(baseUrl + '/deployments/' + encodeURIComponent(args.deployment) + '/functions/' + encodeURIComponent(args.function_name))"
  , "          .then(function(r) { return r.json(); })"
  , "          .then(function(data) { return { content: [{ type: 'text', text: JSON.stringify(data, null, 2) }] }; });"
  , "      }"
  , "    });"
  , ""
  , "    regTool({"
  , "      name: 'evaluate_rule',"
  , "      description: 'Evaluate a legal rule with arguments. Use get_rule_schema first to understand required inputs.',"
  , "      inputSchema: { type: 'object', properties: { deployment: { type: 'string', description: 'Deployment ID' }, function_name: { type: 'string', description: 'Function name' }, arguments: { type: 'object', description: 'Key-value pairs matching the function parameters' } }, required: ['deployment', 'function_name', 'arguments'] },"
  , "      execute: function(args) {"
  , "        return doFetch(baseUrl + '/deployments/' + encodeURIComponent(args.deployment) + '/functions/' + encodeURIComponent(args.function_name) + '/evaluation', {"
  , "          method: 'POST',"
  , "          headers: { 'Content-Type': 'application/json' },"
  , "          body: JSON.stringify({ fnArguments: args.arguments })"
  , "        }).then(function(r) { return r.json(); })"
  , "          .then(function(data) { return { content: [{ type: 'text', text: JSON.stringify(data, null, 2) }] }; });"
  , "      }"
  , "    });"
  , "  }"
  , ""
  , "  function registerDirectTools(baseUrl, fns) {"
  , "    var named = buildToolNames(fns);"
  , "    named.forEach(function(entry) {"
  , "      var fn = entry.fn;"
  , "      var sanitized = sanitizeParamsSchema(fn.parameters);"
  , "      registeredNames[entry.toolName] = true;"
  , "      navigator.modelContext.registerTool({"
  , "        name: entry.toolName,"
  , "        description: (fn.description || 'Evaluate ' + fn.name) + ' [' + fn.deployment + '/' + fn.name + ']',"
  , "        inputSchema: sanitized.schema,"
  , "        execute: function(args) {"
  , "          var originalArgs = remapArgs(args, sanitized.reverseMap);"
  , "          return doFetch(baseUrl + '/deployments/' + encodeURIComponent(fn.deployment) + '/functions/' + encodeURIComponent(fn.name) + '/evaluation', {"
  , "            method: 'POST',"
  , "            headers: { 'Content-Type': 'application/json' },"
  , "            body: JSON.stringify({ fnArguments: originalArgs })"
  , "          }).then(function(r) { return r.json(); })"
  , "            .then(function(data) { return { content: [{ type: 'text', text: JSON.stringify(data, null, 2) }] }; });"
  , "        }"
  , "      });"
  , "    });"
  , "  }"
  , "})();"
  ]
